export function extractQtyAndName(name: string): [number, string] {
  if (!name) return [1, ''];
  name = String(name).trim();
  const m = name.match(/,?\s*(\d+)\s*(박스|개|포|세트)\s*(\([^)]*\))?\s*$/);
  if (m) return [parseInt(m[1]), name.slice(0, m.index).replace(/,\s*$/, '').trim()];
  return [1, name];
}

export function extractQtyFromOption(option: string): number | null {
  if (!option) return null;
  const m = String(option).trim().match(/^(\d+)\s*(박스|개|포)/);
  return m ? parseInt(m[1]) : null;
}

export function applyProductMap(name: string): string {
  return PRODUCT_MAP[name] || name;
}

// 띄어쓰기·쉼표만 다른 변형(몰별 표기 차이)을 흡수하기 위한 정규화 인덱스.
// 정확 매칭이 실패했을 때만 폴백으로 사용. (같은 정규화 키는 항상 같은 대표명 — 충돌 0 검증됨)
const normKey = (s: string) => s.replace(/\s+/g, '').replace(/,/g, '');
const PRODUCT_MAP_NORM: Record<string, string> = (() => {
  const m: Record<string, string> = {};
  for (const k in PRODUCT_MAP) { const nk = normKey(k); if (!(nk in m)) m[nk] = PRODUCT_MAP[k]; }
  return m;
})();

// ── 담당자가 ERP에서 직접 등록하는 런타임 매칭 (DB: product_matches) ──
// 하드코딩 PRODUCT_MAP(검증된 820개)은 그대로 base로 두고, DB 매칭을 '덧씌워' 병합한다.
// → 기존 매칭은 절대 깨지지 않고, 신규/수정만 DB로. DB 항목이 하드코딩보다 우선(담당자 교정 허용).
let DB_MATCH: Record<string, string> = {};
let DB_MATCH_NORM: Record<string, string> = {};
let dbMatchesLoaded = false;

export function setDbMatches(rows: { collect_name?: string; product_name?: string }[]): void {
  const m: Record<string, string> = {};
  const mn: Record<string, string> = {};
  for (const r of rows) {
    const cn = String(r.collect_name || '').trim();
    const pn = String(r.product_name || '').trim();
    if (!cn || !pn) continue;
    m[cn] = pn;
    const nk = normKey(cn);
    if (!(nk in mn)) mn[nk] = pn;
  }
  DB_MATCH = m;
  DB_MATCH_NORM = mn;
  dbMatchesLoaded = true;
}

// 페이지 진입 시 1회 호출 → matchProduct가 DB 매칭까지 반영. 실패해도 하드코딩으로 정상 동작.
export async function loadDbMatches(force = false): Promise<void> {
  if (dbMatchesLoaded && !force) return;
  try {
    const rows = await supabaseFetchAll<{ collect_name: string; product_name: string }>(
      '/product_matches?select=collect_name,product_name',
    );
    setDbMatches(rows);
  } catch { /* 매칭 테이블 없거나 조회 실패해도 하드코딩 매칭으로 동작 */ }
}

// 수집상품명을 매칭데이터 기준 대표상품명으로 변환 + 매칭 여부 반환
// 일자별 출고현황 집계용 — 매칭 안 된 상품은 별도 알림 처리
export function matchProduct(collectName: string): { name: string; matched: boolean } {
  const raw = String(collectName || '').trim();
  if (!raw) return { name: '(상품명 없음)', matched: false };
  const [, cleanName] = extractQtyAndName(raw);
  // 우선순위: DB 정확 → 하드코딩 정확 → DB 정규화 → 하드코딩 정규화
  const mapped =
    DB_MATCH[cleanName] || DB_MATCH[raw]
    || PRODUCT_MAP[cleanName] || PRODUCT_MAP[raw]
    || DB_MATCH_NORM[normKey(cleanName)] || DB_MATCH_NORM[normKey(raw)]
    || PRODUCT_MAP_NORM[normKey(cleanName)] || PRODUCT_MAP_NORM[normKey(raw)];
  return { name: mapped || cleanName || raw, matched: !!mapped };
}

export interface RawOrderRow {
  [key: string]: string | number | undefined;
}

export interface ConvertedOrderRow extends RawOrderRow {
  '상품명': string;
  '수량(주문수량*EA)': number;
  '_is_bundle': 0 | 1;
}

export function convertOrders(raw: RawOrderRow[]): ConvertedOrderRow[] {
  const addrCount: Record<string, number[]> = {};
  raw.forEach((row, idx) => {
    const key = String(row['수취인명'] || '') + '|' + String(row['주소'] || '');
    if (!addrCount[key]) addrCount[key] = [];
    addrCount[key].push(idx);
  });

  const bundleRows = new Set<number>();
  Object.values(addrCount).forEach((indices) => {
    if (indices.length > 1) indices.forEach((i) => bundleRows.add(i));
  });

  const result: ConvertedOrderRow[] = raw.map((row, idx) => {
    const collectName = String(row['★수집상품명'] || row['수집상품명'] || '');
    const collectOpt = String(row['★수집옵션'] || row['수집옵션'] || '');
    const collectQty = parseInt(String(row['★수집수량'] || row['수집수량'] || 1)) || 1;
    const [nameQty, cleanName] = extractQtyAndName(collectName);
    const optQty = extractQtyFromOption(collectOpt);
    const unitQty = optQty !== null ? optQty : nameQty;
    const finalQty = unitQty * collectQty;
    // 상품명 치환은 matchProduct로 통일 (정확 매칭 → 원문 → 띄어쓰기·쉼표 무시 폴백).
    // 변환 저장되는 product_name이 재고/매출 매칭과 100% 동일 기준이 되도록.
    const mappedName = matchProduct(collectName).name;

    return {
      ...row,
      '상품명': mappedName,
      '수량(주문수량*EA)': finalQty,
      '_is_bundle': bundleRows.has(idx) ? 1 : 0,
    };
  });

  result.sort((a, b) => {
    const na = a['상품명'] || '';
    const nb = b['상품명'] || '';
    if (na !== nb) return String(na).localeCompare(String(nb), 'ko');
    return (Number(a['수량(주문수량*EA)']) || 0) - (Number(b['수량(주문수량*EA)']) || 0);
  });

  return result;
}

